package com.pnc.gamestore.model.enums;

// Esta enumeración define las clasificaciones de edad permitidas para un videojuego.
// E    = Everyone (todos)
// E10  = Everyone 10+ (todos mayores de 10)
// T    = Teen (adolescentes)
// M    = Mature (adultos)
// Al ser un enum, Java garantiza que SOLO estos valores pueden existir.
public enum  Classification {
    E,
    E10,
    T,
    M
}
